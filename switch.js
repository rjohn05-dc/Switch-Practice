// Nested If-Else 1


let weather="rainy"

switch(weather){
    case 'sunny':
        console.log("Wear sunglasses")
        break;
    case 'rainy':
        console.log("Take an umbrella")
        break;
    case 'cloudy':
        console.log("Might need a light jacket")
        break;
    case 'snowy':
        console.log("Wear a coat and gloves")
        break;
    default:
        console.log("Weather is unpredictable")
}


